c = ["a", "n", "t", "a", "a", "t", "n", "n", "a", "x", "u", "g", "a", "x", "a"]
i=0
while i<len(c):
    a=c.count("a")
    n=c.count("n")
    t=c.count("t")
    x=c.count("x")
    u=c.count("u")
    g=c.count("g")
    i=i+1
print("a-",a,"time")
print("n-",n,"time")
print("t-",t,"time")
print("x-",x,"time")
print("u-",u,"time")
print("g-",g,"time")

       
